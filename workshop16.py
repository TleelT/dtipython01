# โปรแกรมตรวจสอบรถว่าผ่านเกณฑ์ของการตรวจสอบค่าคาร์บอนไดซ์ออกไซน์หรือ โดยหากค่าคาร์บอนไดซ์ออกไซน์มีค่าเกินกว่า 678.55 
# ให้แสดงข้อความว่า “ไม่ผ่าน”  หากน้อยกว่า 678.55 ให้แสดงข้อความ “ผ่าน” โดยให้รับชื่อเจ้าของ ทะเบียนรถ และปริมาณก๊าซคาร์บอนไดซ์ออกไซน์ที่วัดได้ ทางแป้นพิมพ

# โปรแกรมตรวจสอบค่าคาร์บอนไดออกไซด์ของรถยนต์

# รับข้อมูลจากผู้ใช้
ชื่อเจ้าของ = input("กรุณาใส่ชื่อเจ้าของรถ: ")
ทะเบียนรถ = input("กรุณาใส่หมายเลขทะเบียนรถ: ")
try:
    ปริมาณ_CO2 = float(input("กรุณาใส่ปริมาณก๊าซคาร์บอนไดออกไซด์ที่วัดได้: "))
except ValueError:
    print("กรุณาใส่ตัวเลขที่ถูกต้องสำหรับค่าคาร์บอนไดออกไซด์")
    exit()

# ตรวจสอบค่าคาร์บอนไดออกไซด์
เกณฑ์ = 678.55

if ปริมาณ_CO2 > เกณฑ์:
    ผล = "ไม่ผ่าน"
else:
    ผล = "ผ่าน"

# แสดงผลลัพธ์
print("\nผลการตรวจสอบรถยนต์")
print("-----------------------")
print(f"เจ้าของรถ: {ชื่อเจ้าของ}")
print(f"ทะเบียนรถ: {ทะเบียนรถ}")
print(f"ค่าคาร์บอนไดออกไซด์: {ปริมาณ_CO2}")
print(f"ผลการตรวจสอบ: {ผล}")