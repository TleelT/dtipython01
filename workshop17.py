# โปรแกรมซึ่งรับชื่อ และปีเกิดของผู้ใช้โปรแกรมทางแป้นพิมพ์ โดยหากอายุมีค่าตั้งแต่ 35 ปีขึ้นไป 
# ให้แสดงข้อความว่า "คุณแก่แล้ว..." หากไม่ถึงให้แสดงข้อความ "คุณยังไม่แก่..." ทางหน้าจอ

# โปรแกรมตรวจสอบอายุ (ปี พ.ศ.)

# รับชื่อ
name = input("กรุณาใส่ชื่อของคุณ: ")

# รับปีเกิด พ.ศ.
birth_year = int(input("กรุณาใส่ปีเกิดของคุณ (พ.ศ.): "))

# คำนวณอายุ
current_year = 2568  # สมมติปีปัจจุบันเป็น พ.ศ. 2568
age = current_year - birth_year

# แสดงผลตามอายุ
if age >= 35:
    print("คุณแก่แล้ว...")
else:
    print("คุณยังไม่แก่...")